
# Operations using Arithmatic Operators
#####################################################################

# Adding two numbers using addition operators
a = 10 
b = 20 
sum_of_two_numbers = a + b
print(sum_of_two_numbers)

# Subtraction Operator: Subtract second number from the first
firstNum = 30
secondNum = 40
difference_of_nums = firstNum - secondNum
print(difference_of_nums)

# Multiplication of two numbers
product_of_two_nums = firstNum * secondNum
print(product_of_two_nums)

# Divisio of two numbers

firstNum = 43
secondNum = 8
normal_division = firstNum / secondNum
print(f"Result of Normal division : {normal_division}")
floor_division = firstNum // secondNum
print(f"Result of floor division: {floor_division}") # Returns the qoutient
modulus_of_two_nums = firstNum % secondNum
print(f"Remainder after dividing firstNum by secondNum {modulus_of_two_nums}") # Returns remainder

# Exponent values
firstNum = 2
secondNum = 100

sqaure_nums = firstNum ** secondNum
print(sqaure_nums)

